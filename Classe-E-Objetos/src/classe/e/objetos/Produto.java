package classe.e.objetos;


public class Produto {
    
    private int id;
    private String nome;
    private String dec;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    
    
    public String getNome() {
        return nome;
    }
    
    

    public void setNome(String nome) {
        this.nome = nome;
    }

    
    
    public String getDec() {
        return dec;
    }

    
    
    public void setDec(String dec) {
        this.dec = dec;
    }
    
     
}
